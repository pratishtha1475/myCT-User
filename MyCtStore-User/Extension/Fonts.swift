import Foundation
import UIKit

extension UIFont {
    static func rubikBold(size: CGFloat) -> UIFont {
        return UIFont(name: "Rubik-Bold", size: size)!
    }
    static func rubikLight(size: CGFloat) -> UIFont {
        return UIFont(name: "Rubik-Light", size: size)!
    }
    static func rubikMedium(size: CGFloat) -> UIFont {
        return UIFont(name: "Rubik-Medium", size: size)!
    }
    static func rubikSemiBold(size: CGFloat) -> UIFont {
        return UIFont(name: "Rubik-SemiBold", size: size)!
    }
    static func rubikRegular(size: CGFloat) -> UIFont {
        return UIFont(name: "Rubik-Regular", size: size)!
    }
}
